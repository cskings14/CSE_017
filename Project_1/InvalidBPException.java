public class InvalidBPException extends Exception {
    

    
    public InvalidBPException(String message) {
        super(message);
    }
    
    public InvalidBPException(String message, Throwable cause) {
        super(message, cause);
    }
    
    public InvalidBPException(Throwable cause) {
        super(cause);
    }
    
    public InvalidBPException() {
        super();
    }
    
}
    

